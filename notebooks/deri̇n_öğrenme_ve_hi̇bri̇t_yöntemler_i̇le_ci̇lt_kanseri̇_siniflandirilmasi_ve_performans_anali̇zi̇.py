import torch
import torch.nn as nn
import torchvision.models as models
import time

# GPU Kontrolü
device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
print(f"Modelin eğitileceği cihaz: {device}")

# ResNet50 Modelini Ön Eğitimli Olarak Yükleme
resnet50 = models.resnet50(weights=models.ResNet50_Weights.DEFAULT)

# Son katmanı 7 sınıfa göre güncelle
num_features = resnet50.fc.in_features
resnet50.fc = nn.Linear(num_features, 7)

# Modeli GPU belleğine taşı
resnet50 = resnet50.to(device)

# Kayıp Fonksiyonu ve Optimizer Tanımlama
criterion = nn.CrossEntropyLoss()
optimizer = torch.optim.Adam(resnet50.parameters(), lr=1e-4) # Medikal transfer öğrenme için ideal learning rate

# Eğitim Döngüsü Parameteresi
EPOCHS = 15
history = {'train_loss': [], 'train_acc': [], 'val_loss': [], 'val_acc': []}

for epoch in range(EPOCHS):
    start_time = time.time()

    # Eğitim
    resnet50.train()
    train_loss, train_corrects = 0.0, 0

    for images, labels in train_loader:
        images, labels = images.to(device), labels.to(device)

        optimizer.zero_grad() # Gradymantları sıfırla
        outputs = resnet50(images) # İleri besleme
        loss = criterion(outputs, labels) # Kayıp hesapla

        loss.backward() # Geriye yayılım
        optimizer.step() # Ağırlıkları güncelle

        _, preds = torch.max(outputs, 1)
        train_loss += loss.item() * images.size(0)
        train_corrects += torch.sum(preds == labels.data)

    epoch_train_loss = train_loss / len(train_loader.dataset)
    epoch_train_acc = train_corrects.double() / len(train_loader.dataset)

    # Doğrulama
    resnet50.eval()
    val_loss, val_corrects = 0.0, 0

    with torch.no_grad(): # Eğitimi durdur, hafızayı rahatlat
        for images, labels in val_loader:
            images, labels = images.to(device), labels.to(device)
            outputs = resnet50(images)
            loss = criterion(outputs, labels)

            _, preds = torch.max(outputs, 1)
            val_loss += loss.item() * images.size(0)
            val_corrects += torch.sum(preds == labels.data)

    epoch_val_loss = val_loss / len(val_loader.dataset)
    epoch_val_acc = val_corrects.double() / len(val_loader.dataset)

    # Geçmişi kaydet
    history['train_loss'].append(epoch_train_loss)
    history['train_acc'].append(epoch_train_acc.item())
    history['val_loss'].append(epoch_val_loss)
    history['val_acc'].append(epoch_val_acc.item())

    epoch_mins, epoch_secs = divmod(time.time() - start_time, 60)
    print(f"Epoch {epoch+1}/{EPOCHS} [{int(epoch_mins)}m {int(epoch_secs)}s] -> "
          f"Train Loss: {epoch_train_loss:.4f} | Train Acc: {epoch_train_acc:.4f} || "
          f"Val Loss: {epoch_val_loss:.4f} | Val Acc: {epoch_val_acc:.4f}")

# Kaybederşm falan diye kaydettim
torch.save(resnet50.state_dict(), 'resnet50_ham10000.pth')

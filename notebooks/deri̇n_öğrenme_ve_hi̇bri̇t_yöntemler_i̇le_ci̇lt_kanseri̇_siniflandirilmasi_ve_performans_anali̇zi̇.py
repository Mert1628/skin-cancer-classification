# Giriş boyutunu belirliyoruz
IMG_SIZE = 224 # ideak buymuş

# Eğitim seti için Veri Artırımı (Data Augmentation) içeren transformasyonlar
train_transform = transforms.Compose([
    transforms.Resize((IMG_SIZE, IMG_SIZE)), # Görüntüyü yeniden boyutlandır
    transforms.RandomHorizontalFlip(p=0.5), # Rastgele yatay çevirme
    transforms.RandomVerticalFlip(p=0.5),   # Rastgele dikey çevirme
    transforms.RandomRotation(degrees=20),   # Rastgele 20 dereceye kadar döndürme
    transforms.ColorJitter(brightness=0.1, contrast=0.1, saturation=0.1), # Hafif renk/ışık değişimi
    transforms.ToTensor(), # Görseli PyTorch Tensörüne çevirir
    transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]) # ImageNet standart normalizasyonu
])

# Doğrulama ve Test setleri için sadece boyutlandırma ve normalizasyon
val_test_transform = transforms.Compose([
    transforms.Resize((IMG_SIZE, IMG_SIZE)),
    transforms.ToTensor(),
    transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
])

# Klasik HAM10000 PyTorch Sınıfı
class HAM10000Dataset(Dataset):
    def __init__(self, dataframe, transform=None):
        self.df = dataframe
        self.transform = transform

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        # Resmin bilgisayardaki tam yolunu aldım
        img_path = self.df.iloc[idx]['image_path']
        # Resmi RGB formatında açtım
        image = Image.open(img_path).convert('RGB')
        # Etiket bilgisini alma
        label = int(self.df.iloc[idx]['label'])

        if self.transform:
            image = self.transform(image)

        return image, label

# Dataset nesnelerini oluşturma
train_dataset = HAM10000Dataset(train_df, transform=train_transform)
val_dataset = HAM10000Dataset(val_df, transform=val_test_transform)

import numpy as np
import copy
from sklearn.svm import SVC
from sklearn.metrics import accuracy_score


# Eğittiğimiz EfficientNet modelinin bir kopyasını alıp classifier katmanını söktüm
feature_extractor = copy.deepcopy(efficientnet_b0)
feature_extractor.classifier = nn.Identity() # Classifier yerine Identity koyarak sadece ham özellikleri aldım
feature_extractor.eval() # Gradyan takibini kapattım

# Verileri ve Etiketleri Toplamafonksiyonu
def extract_features(data_loader):
    features_list = []
    labels_list = []

    with torch.no_grad():
        for images, labels in data_loader:
            images = images.to(device)
            # Resimleri modelden geçirip vektörleri al
            feats = feature_extractor(images)

            # Verileri CPU'ya ve Numpy formatına çevir
            features_list.append(feats.cpu().numpy())
            labels_list.append(labels.numpy())

    # Tüm paketleri tek bir büyük matrise dönüştür
    return np.concatenate(features_list, axis=0), np.concatenate(labels_list, axis=0)

#Eğitim setinin derin özellikleri çıkart
X_train, y_train = extract_features(train_loader)

